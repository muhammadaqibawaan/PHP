function count_row($dbname){
  global $connection;
  $query = "SELECT * FROM $dbname";
  $result = mysqli_query($connection, $query);
  $count = mysqli_num_rows($result);
  return $count;
}

function count_row_custom($dbname, $condition, $value){
  global $connection;
  $query = "SELECT * FROM $dbname WHERE $condition='{$value}'";
  $result = mysqli_query($connection, $query);
  $count = mysqli_num_rows($result);
  return $count;
}

